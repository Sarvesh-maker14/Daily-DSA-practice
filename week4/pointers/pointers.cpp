// address ko store karne ke liye pointers

#include<iostream>
using namespace std;

int main()
{
    int x =4;
    int* p = &x;  // p me store kiya x ka address
                  // int* is a datatype
    cout<<&x;
    cout<<endl;
    cout<<p;
    cout<<endl;
    cout<<&p; // pointer ka address





    return 0;
}